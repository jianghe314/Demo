public class RecommendNews {
    private String info_title;
    private String publish_time;
    private String info_image;
    private String info_intro;
    private String consult_id;

    public String getInfo_title() {
        return info_title;
    }

    public void setInfo_title(String info_title) {
        this.info_title = info_title;
    }

    public String getPublish_time() {
        return publish_time;
    }

    public void setPublish_time(String publish_time) {
        this.publish_time = publish_time;
    }

    public String getInfo_image() {
        return info_image;
    }

    public void setInfo_image(String info_image) {
        this.info_image = info_image;
    }

    public String getInfo_intro() {
        return info_intro;
    }

    public void setInfo_intro(String info_intro) {
        this.info_intro = info_intro;
    }

    public String getConsult_id() {
        return consult_id;
    }

    public void setConsult_id(String consult_id) {
        this.consult_id = consult_id;
    }
}
