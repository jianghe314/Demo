public class RecommendSchool {
    private String schoolrecommend_id;
    private String name;
    private String address;
    private String telephone;
    private String photo;

    public String getSchoolrecommend_id() {
        return schoolrecommend_id;
    }

    public void setSchoolrecommend_id(String schoolrecommend_id) {
        this.schoolrecommend_id = schoolrecommend_id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getTelephone() {
        return telephone;
    }

    public void setTelephone(String telephone) {
        this.telephone = telephone;
    }

    public String getPhoto() {
        return photo;
    }

    public void setPhoto(String photo) {
        this.photo = photo;
    }
}
