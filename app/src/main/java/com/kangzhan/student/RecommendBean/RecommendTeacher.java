public class RecommendTeacher {
    private String name;
    private String driving_year;
    private String driving_school;
    private String photo;
    private String intro;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDriving_year() {
        return driving_year;
    }

    public void setDriving_year(String driving_year) {
        this.driving_year = driving_year;
    }

    public String getDriving_school() {
        return driving_school;
    }

    public void setDriving_school(String driving_school) {
        this.driving_school = driving_school;
    }

    public String getPhoto() {
        return photo;
    }

    public void setPhoto(String photo) {
        this.photo = photo;
    }

    public String getIntro() {
        return intro;
    }

    public void setIntro(String intro) {
        this.intro = intro;
    }
}
