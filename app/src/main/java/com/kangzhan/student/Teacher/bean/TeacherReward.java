public class TeacherReward {
    private String id;
    private String amount;
    private String bonus_month;
    private String keer;
    private String kesan;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getAmount() {
        return amount;
    }

    public void setAmount(String amount) {
        this.amount = amount;
    }

    public String getBonus_month() {
        return bonus_month;
    }

    public void setBonus_month(String bonus_month) {
        this.bonus_month = bonus_month;
    }

    public String getKeer() {
        return keer;
    }

    public void setKeer(String keer) {
        this.keer = keer;
    }

    public String getKesan() {
        return kesan;
    }

    public void setKesan(String kesan) {
        this.kesan = kesan;
    }
}
