public class AccountAffirm {
    private String id;
    private String time;
    private String amount;
    private String train_length;
    private String draw;
    private String attach_amount;
    private String attach_train_length;
    private String attach_draw;
    private String status;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getAmount() {
        return amount;
    }

    public void setAmount(String amount) {
        this.amount = amount;
    }

    public String getTrain_length() {
        return train_length;
    }

    public void setTrain_length(String train_length) {
        this.train_length = train_length;
    }

    public String getDraw() {
        return draw;
    }

    public void setDraw(String draw) {
        this.draw = draw;
    }

    public String getAttach_amount() {
        return attach_amount;
    }

    public void setAttach_amount(String attach_amount) {
        this.attach_amount = attach_amount;
    }

    public String getAttach_train_length() {
        return attach_train_length;
    }

    public void setAttach_train_length(String attach_train_length) {
        this.attach_train_length = attach_train_length;
    }

    public String getAttach_draw() {
        return attach_draw;
    }

    public void setAttach_draw(String attach_draw) {
        this.attach_draw = attach_draw;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
}
