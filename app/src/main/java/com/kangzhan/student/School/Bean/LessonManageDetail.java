public class LessonManageDetail {
    private String name;
    private String class_fee;
    private String max_stu;
    private String duration;
    private String stage;
    private String price;
    private String traintype;
    private String start_time;
    private String end_time;
    private String status;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getClass_fee() {
        return class_fee;
    }

    public void setClass_fee(String class_fee) {
        this.class_fee = class_fee;
    }

    public String getMax_stu() {
        return max_stu;
    }

    public void setMax_stu(String max_stu) {
        this.max_stu = max_stu;
    }

    public String getDuration() {
        return duration;
    }

    public void setDuration(String duration) {
        this.duration = duration;
    }

    public String getStage() {
        return stage;
    }

    public void setStage(String stage) {
        this.stage = stage;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getTraintype() {
        return traintype;
    }

    public void setTraintype(String traintype) {
        this.traintype = traintype;
    }

    public String getStart_time() {
        return start_time;
    }

    public void setStart_time(String start_time) {
        this.start_time = start_time;
    }

    public String getEnd_time() {
        return end_time;
    }

    public void setEnd_time(String end_time) {
        this.end_time = end_time;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
}
